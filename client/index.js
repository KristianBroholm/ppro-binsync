btnSelect.addEventListener('click', () => {
    try {
        const result = window.cep.fs.showOpenDialog(false, true, "Select Folder to Watch", "", []);
        if (result.data && result.data.length > 0) {
            let selectedPath = result.data[0];
            if (selectedPath.indexOf('file://') === 0) {
                selectedPath = decodeURI(selectedPath.replace('file://', ''));
                if (process.platform === 'win32' && selectedPath.indexOf('/') === 0) {
                    selectedPath = selectedPath.substring(1);
                }
            }
            watchFolder = selectedPath;
            statusText.textContent = `Watching: ${watchFolder}`;
            btnToggle.disabled = false;
            log(`Target set: ${watchFolder}`);
        }
    } catch (e) {
        log("Dialog Error: " + e.message, "error");
    }
});

btnToggle.addEventListener('click', () => {
    if (isWatching) {
        stopWatching();
    } else {
        startWatching();
    }
});

/**
 * Recursively scans a directory and imports all files.
 * Process files in current directory FIRST, then recurse into subdirectories.
 * This ensures high-res media exists before its proxies are attached.
 */
async function scanAndImport(dir, rootDir) {
    const items = fs.readdirSync(dir);
    
    // Ensure the current folder exists as a bin in Premiere
    if (dir !== rootDir) {
        const relativePath = path.relative(rootDir, dir);
        const folderName = path.basename(dir).toLowerCase();
        
        // Skip creating bins for special proxy folders
        if (folderName === 'proxy' || folderName === 'proxies') {
            // Do not create bin, but still scan children for attachment
        } else {
            await handleFolderAdded(relativePath);
        }
    }

    const subdirs = [];
    const files = [];

    for (const item of items) {
        const fullPath = path.join(dir, item);
        const stat = fs.statSync(fullPath);
        if (stat.isDirectory()) {
            if (!EXCLUDED_FOLDERS.includes(item)) {
                subdirs.push(fullPath);
            }
        } else {
            // Ignore system files
            if (!item.startsWith('.') && !item.endsWith('.prlock') && !item.endsWith('.prproj')) {
                files.push({ fullPath, item });
            }
        }
    }

    // 1. Process all local files first
    for (const fileObj of files) {
        const relativePath = path.relative(rootDir, fileObj.fullPath);
        await handleFileAdded(fileObj.fullPath, relativePath);
    }

    // 2. Then recurse into subdirectories
    for (const subdir of subdirs) {
        await scanAndImport(subdir, rootDir);
    }
}

function startWatching() {
    if (!watchFolder) return;
    
    ensureHostLoaded(async (success) => {
        if (!success) return;
        
        try {
            isWatching = true;
            btnToggle.textContent = "Stop Watching";
            btnToggle.classList.add('active');
            
            log(`Starting initial scan of: ${watchFolder}...`);
            await scanAndImport(watchFolder, watchFolder);
            log(`Initial scan complete. Watcher active.`, "success");

            watcher = fs.watch(watchFolder, { recursive: true }, (eventType, filename) => {
                if (filename && eventType === 'rename') {
                    const fullPath = path.join(watchFolder, filename);
                    
                    if (isExcluded(filename)) return;
                    if (filename.endsWith('.prlock') || filename.endsWith('.prproj')) return;

                    // If we are already processing this exact path, ignore the duplicate event
                    if (processingFiles.has(fullPath)) return;

                    setTimeout(() => {
                        if (fs.existsSync(fullPath)) {
                            const isHidden = filename.split(path.sep).some(part => part.startsWith('.'));
                            if (isHidden) return;

                            if (fs.lstatSync(fullPath).isDirectory()) {
                                handleFolderAdded(filename);
                            } else {
                                handleFileAdded(fullPath, filename);
                            }
                        }
                    }, 200);
                }
            });
        } catch (e) {
            log("Watcher Error: " + e.message, "error");
            stopWatching();
        }
    });
}

function stopWatching() {
    if (watcher) {
        watcher.close();
        watcher = null;
    }
    isWatching = false;
    btnToggle.textContent = "Start Watching";
    btnToggle.classList.remove('active');
    log(`Watcher stopped.`);
    processingFiles.clear();
}

/**
 * Calls ExtendScript to create or verify a bin hierarchy.
 */
function handleFolderAdded(relativePath) {
    const parts = relativePath.split(path.sep);
    if (parts.some(p => {
        const lp = p.toLowerCase();
        return lp === 'proxy' || lp === 'proxies';
    })) {
        return Promise.resolve();
    }

    return new Promise((resolve) => {
        const normalizedRelative = relativePath.replace(/\\/g, '/').replace(/^\.\//, '');
        const script = `$._ext_PPRO.getOrCreateBin("${normalizedRelative}")`;
        csInterface.evalScript(script, (result) => {
            if (result === "success") {
                log(`Verified Bin: ${normalizedRelative}`, "success");
            }
            resolve();
        });
    });
}

/**
 * Normalizes paths and calls ExtendScript to import the file or attach as proxy.
 */
function handleFileAdded(fullPath, relativePath) {
    if (processingFiles.has(fullPath)) return Promise.resolve();
    processingFiles.add(fullPath);

    return new Promise((resolve) => {
        const normalizedFull = fullPath.replace(/\\/g, '/');
        const filename = path.basename(fullPath);
        const proxyDir = path.dirname(fullPath);
        const folderName = path.basename(proxyDir).toLowerCase();

        // CHECK IF IT SHOULD BE A PROXY
        if (folderName === 'proxy' || folderName === 'proxies') {
            const parentDir = path.dirname(proxyDir);
            const highResPath = path.join(parentDir, filename).replace(/\\/g, '/');

            log(`Attaching Proxy: ${filename}...`);
            const script = `$._ext_PPRO.attachProxyByPath("${normalizedFull}", "${highResPath}")`;
            csInterface.evalScript(script, (result) => {
                processingFiles.delete(fullPath);
                if (result === "true") {
                    log(`Attached Proxy: ${filename}`, "success");
                } else {
                    log(`Proxy skipped: No matching media found for ${filename}`);
                }
                resolve();
            });
            return;
        }

        // NORMAL IMPORT LOGIC
        const fileHash = getShallowHash(fullPath);
        if (fileHash && sessionHashes.has(fileHash)) {
            log(`Skipped: ${relativePath} (Duplicate content detected via hash)`);
            processingFiles.delete(fullPath);
            return resolve();
        }

        const normalizedRelative = relativePath.replace(/\\/g, '/').replace(/^\.\//, '');
        const pathParts = normalizedRelative.split('/');
        pathParts.pop(); 
        const binHierarchy = pathParts.join('/');

        const script = `$._ext_PPRO.importFileToBin("${normalizedFull}", "${binHierarchy}")`;
        
        csInterface.evalScript(script, (result) => {
            processingFiles.delete(fullPath);

            if (result === "EvalScript error.") {
                log(`Critical Error: script execution failed for ${normalizedRelative}`, "error");
            } else if (result === "exists") {
                log(`Skipped: ${normalizedRelative} (Path already in project)`);
                if (fileHash) sessionHashes.add(fileHash);
            } else if (result.indexOf("Error:") === 0) {
                log(`${result} (${normalizedRelative})`, "error");
            } else if (result === "false") {
                log(`Import failed for: ${normalizedRelative}`, "error");
            } else if (result === "true") {
                log(`Imported: ${normalizedRelative}`, "success");
                if (fileHash) sessionHashes.add(fileHash);
            }
            resolve();
        });
    });
}
