const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const csInterface = new CSInterface();

let watchFolder = null;
let watcher = null;
let isWatching = false;
let processingFiles = new Set(); // Track files currently being imported
let sessionHashes = new Set();  // Track unique file signatures in this session

const btnSelect = document.getElementById('btn-select');
const btnToggle = document.getElementById('btn-toggle');
const btnTest = document.getElementById('btn-test');
const btnReload = document.getElementById('btn-reload');
const chkAutoCopy = document.getElementById('chk-auto-copy');
const statusText = document.getElementById('id-status-text');
const logContainer = document.getElementById('log-container');

/**
 * Event Listener for items added to project (dispatched from host/index.jsx)
 */
csInterface.addEventListener("com.binsync.itemAdded", (event) => {
    if (!chkAutoCopy.checked || !watchFolder) return;
    
    try {
        const data = JSON.parse(event.data);
        const mediaPath = data.mediaPath;
        const binPath = data.binPath; // e.g. "Footage/B-Roll"
        const name = data.name;

        // Correctly check if the file is already inside the watch folder
        const relativeToWatch = path.relative(watchFolder, mediaPath);
        const isInsideWatch = relativeToWatch && !relativeToWatch.startsWith('..') && !path.isAbsolute(relativeToWatch);

        if (isInsideWatch) {
            return;
        }

        log(`External Import Detected: ${name}`);

        
        // Construct target path
        const targetDir = path.join(watchFolder, binPath);
        const targetPath = path.join(targetDir, name);

        // Ensure directory exists
        if (!fs.existsSync(targetDir)) {
            fs.mkdirSync(targetDir, { recursive: true });
        }

        // Check if destination already exists (maybe same name, different content?)
        if (fs.existsSync(targetPath)) {
            const sourceHash = getShallowHash(mediaPath);
            const targetHash = getShallowHash(targetPath);
            if (sourceHash === targetHash) {
                log(`Skipped Copy: ${name} (File already in watch folder)`);
                return;
            } else {
                // Same name but different - append suffix or ignore?
                // For safety, let's not overwrite. Add timestamp.
                const ext = path.extname(name);
                const base = path.basename(name, ext);
                const newName = `${base}_${Date.now()}${ext}`;
                const newTargetPath = path.join(targetDir, newName);
                
                log(`Copying with unique name: ${newName}...`);
                fs.copyFileSync(mediaPath, newTargetPath);
                
                // Add to session hashes so the watcher skips it
                const hash = getShallowHash(mediaPath);
                if (hash) sessionHashes.add(hash);
                log(`Copied: ${newName} to watch folder.`, "success");
                return;
            }
        }

        log(`Copying to watch folder: ${name}...`);
        fs.copyFileSync(mediaPath, targetPath);
        
        // Add to session hashes so the watcher skips it
        const hash = getShallowHash(mediaPath);
        if (hash) sessionHashes.add(hash);
        
        log(`Copied: ${name} to watch folder.`, "success");

    } catch (e) {
        log("Auto-copy Error: " + e.message, "error");
    }
});

/**
 * Generates a "shallow" hash of a file for fast duplicate detection.
 * Samples the start, middle, and end of the file + file size.
 */
function getShallowHash(filePath) {
    try {
        const stats = fs.statSync(filePath);
        if (stats.isDirectory()) return null;

        const size = stats.size;
        const SAMPLE_SIZE = 65536; // 64KB
        const buffer = Buffer.alloc(SAMPLE_SIZE * 3 + 8); // 3 samples + 8 bytes for size
        
        const fd = fs.openSync(filePath, 'r');
        
        // Sample 1: Start
        fs.readSync(fd, buffer, 0, SAMPLE_SIZE, 0);
        
        // Sample 2: Middle
        const middlePos = Math.floor(size / 2);
        fs.readSync(fd, buffer, SAMPLE_SIZE, SAMPLE_SIZE, middlePos);
        
        // Sample 3: End
        const endPos = Math.max(0, size - SAMPLE_SIZE);
        fs.readSync(fd, buffer, SAMPLE_SIZE * 2, SAMPLE_SIZE, endPos);
        
        // Add size to the end of buffer
        buffer.writeBigInt64BE(BigInt(size), SAMPLE_SIZE * 3);
        
        fs.closeSync(fd);
        
        return crypto.createHash('md5').update(buffer).digest('hex');
    } catch (e) {
        console.error("Hash error:", e);
        return null;
    }
}

// Folders generated by Premiere Pro or system that should be ignored
const EXCLUDED_FOLDERS = [
    'Adobe Premiere Pro Auto-Save',
    'Adobe Premiere Pro Captured Audio',
    'Adobe Premiere Pro Captured Video',
    'Adobe Premiere Pro Video Previews',
    'Adobe Premiere Pro Audio Previews',
    'Encoded Files',
    'Media Cache Files',
    'Peak Files'
];

function log(msg, type = '') {
    const div = document.createElement('div');
    if (type) div.className = type;
    div.textContent = `[${new Date().toLocaleTimeString()}] ${msg}`;
    logContainer.appendChild(div);
    logContainer.scrollTop = logContainer.scrollHeight;
}

/**
 * Checks if a path contains any excluded Adobe folders.
 */
function isExcluded(filePath) {
    const parts = filePath.split(path.sep);
    return parts.some(part => EXCLUDED_FOLDERS.includes(part));
}

/**
 * Ensures the ExtendScript host is loaded.
 */
function ensureHostLoaded(callback) {
    csInterface.evalScript('typeof($._ext_PPRO)', (result) => {
        if (result === 'undefined') {
            log("Host script not loaded. Attempting to load host/index.jsx...");
            const extensionPath = csInterface.getSystemPath(SystemPath.EXTENSION);
            const scriptPath = path.join(extensionPath, 'host', 'index.jsx').replace(/\\/g, '/');
            const loadScript = `$.evalFile("${scriptPath}")`;
            csInterface.evalScript(loadScript, (loadResult) => {
                if (loadResult === "EvalScript error.") {
                    log("Critical Error: Failed to load host/index.jsx via evalFile.", "error");
                    callback(false);
                } else {
                    log("Host script loaded manually.", "success");
                    callback(true);
                }
            });
        } else {
            callback(true);
        }
    });
}

